	public static class Question
	{
		public static void Exec()
		{
			var array = Console.ReadLine().Split(' ').Select(i => int.Parse(i)).ToArray();
			int m = array[0];
			int n = array[1];
			int N = array[2];

			int total = N;
			int answer = N;

			while (total >= m) {
				answer += total / m * n;
				total = total / m * n + total % m;
				//Console.WriteLine($"{total} {answer}");
			}

			Console.WriteLine($"{answer}");
			Console.ReadKey();
		}
	}
