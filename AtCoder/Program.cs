			long answer = 0;
			long[] array = new long[ln + 1];
			array[0] = 2;
			array[1] = 1;
			for (var i = 2; i <= ln; ++i) {
				array[i] = array[i - 1] + array[i - 2];
			}

			answer = array[ln];

			Console.WriteLine($"{answer}");
			

			Console.ReadKey();
		}
	}
